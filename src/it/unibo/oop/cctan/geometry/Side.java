package it.unibo.oop.cctan.geometry;

/**
 * Identify the side of an element in the game area.
 */
public enum Side {
    /**
     * The upper side of the element.
     */
    ABOVE,
    /**
     * The lower side of the element.
     */
    BELOW,
    /**
     * The right side of the element.
     */
    RIGHT,
    /**
     * The left side of the element.
     */
    LEFT,
    /**
     * The corner (a vertex).
     */
    CORNER;
}
